        <h3>Badulla-Chenkalady (A-5) Road</h3>
        <p><b>Client:Road Development Authority (RDA)<br>
          Completion Date:January 2021<br>
           Location:Batticaloa District<br>
          Project Manager:Mr. H G P Jeewantha</b></p> <br>
         <p>Funded by the OPEC Fund for International Development (OFID) and the Saudi Fund for Development (SFD), the Badulla-Chenkaladi Road Improvement Project (BCRIP) was initiated to improve connectivity between the Eastern Province, Uva Province, and Central Province, which are home to nearly three million people.<br><br>
 The project set out to rehabilitate 151.3 km of the A-5 Peradeniya-Badulla-Chenkaladi Road, and is expected to improve road safety, facilitate access to markets, social services, and job opportunities. After completion, the rehabilitated road will lead to lower transport costs of agricultural produce and industrials goods, which in turn is expected to enhance trade and stimulate the manufacturing, mining, tourism and agriculture sectors. In the long term, development of this critical road bridging three provinces is expected to improve quality of life while increasing financial returns to farmers and local communities.<br><br>
 BuildMaster’s scope of work involved rehabilitation of a total road length of 27 km from Tampitiya to Chenkaladi, including all clearing, grubbing, roadway excavation, sub-base construction, construction of drainage and shoulders, and laying of a base-course and asphalt wearing course. The project also included rehabilitation and construction of 88 culverts and 22 bridges, and installation of road furniture including road marking and other incidental work.<br><br>
 The project was successfully completed and handed over to local communities in January 2021.</p>
